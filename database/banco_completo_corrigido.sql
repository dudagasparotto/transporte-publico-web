-- Banco completo utilizado pelo OminiBus
-- A exclusão de rotas remove seus pontos e respectivos horários.
SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS = 0;

-- DELETE / DROP DE TODAS AS TABELAS
DROP TABLE IF EXISTS `onibus`;
DROP TABLE IF EXISTS `rota_onibus`;
DROP TABLE IF EXISTS `localizacao`;
DROP TABLE IF EXISTS `motoristas_rotas`;
DROP TABLE IF EXISTS `avaliacao`;
DROP TABLE IF EXISTS `horarios`;
DROP TABLE IF EXISTS `pontos`;
DROP TABLE IF EXISTS `rotas`;
DROP TABLE IF EXISTS `linhas`;
DROP TABLE IF EXISTS `usuarios`;
DROP TABLE IF EXISTS `motorista`;
DROP TABLE IF EXISTS `tipo_usuarios`;

-- CREATE DAS TABELAS
CREATE TABLE `tipo_usuarios` (
  `id_tipo_usuario` tinyint(4) NOT NULL AUTO_INCREMENT,
  `nome_tipo_usuario` varchar(20) NOT NULL,
  PRIMARY KEY (`id_tipo_usuario`)
) ENGINE=InnoDB AUTO_INCREMENT=3 DEFAULT CHARSET=utf8;

CREATE TABLE `motorista` (
  `id_motorista` int(11) NOT NULL,
  `nome_motorista` varchar(100) DEFAULT NULL,
  `cpf_motorista` varchar(20) DEFAULT NULL,
  `cnh_motorista` varchar(20) DEFAULT NULL,
  `foto_motorista` varchar(255) DEFAULT NULL,
  `rota_motorista` mediumint(9) DEFAULT NULL,
  PRIMARY KEY (`id_motorista`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `usuarios` (
  `id_usuario` int(11) NOT NULL AUTO_INCREMENT,
  `id_tipo_usuario` tinyint(4) NOT NULL,
  `id_motorista` int(11) DEFAULT NULL,
  `nome_usuario` varchar(80) NOT NULL,
  `senha_usuario` varchar(128) NOT NULL,
  PRIMARY KEY (`id_usuario`),
  UNIQUE KEY `uk_usuarios_id_motorista` (`id_motorista`),
  KEY `id_tipo_usuario` (`id_tipo_usuario`),
  CONSTRAINT `fk_usuarios_motorista` FOREIGN KEY (`id_motorista`) REFERENCES `motorista` (`id_motorista`) ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT `usuarios_ibfk_1` FOREIGN KEY (`id_tipo_usuario`) REFERENCES `tipo_usuarios` (`id_tipo_usuario`)
) ENGINE=InnoDB AUTO_INCREMENT=25 DEFAULT CHARSET=utf8;

CREATE TABLE `linhas` (
  `id_linha` smallint(6) NOT NULL AUTO_INCREMENT,
  `nome_linhas` varchar(30) NOT NULL,
  PRIMARY KEY (`id_linha`)
) ENGINE=InnoDB AUTO_INCREMENT=8 DEFAULT CHARSET=utf8;

CREATE TABLE `rotas` (
  `id_rota` mediumint(9) NOT NULL,
  `id_linha` smallint(6) NOT NULL,
  `cor` varchar(7) NOT NULL DEFAULT '#6B7280',
  `trajeto` longtext,
  PRIMARY KEY (`id_rota`),
  KEY `id_linha` (`id_linha`),
  CONSTRAINT `rotas_ibfk_2` FOREIGN KEY (`id_linha`) REFERENCES `linhas` (`id_linha`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `pontos` (
  `id_pontos` smallint(6) NOT NULL AUTO_INCREMENT,
  `nome_pontos` varchar(100) NOT NULL,
  `latitude_pontos` decimal(11,8) NOT NULL,
  `longitude_pontos` decimal(11,8) NOT NULL,
  `id_rota` mediumint(9) NOT NULL,
  PRIMARY KEY (`id_pontos`),
  KEY `fk_pontos_rota` (`id_rota`),
  CONSTRAINT `fk_pontos_rota` FOREIGN KEY (`id_rota`) REFERENCES `rotas` (`id_rota`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB AUTO_INCREMENT=47 DEFAULT CHARSET=utf8;

CREATE TABLE `horarios` (
  `id_horario` mediumint(9) NOT NULL AUTO_INCREMENT,
  `id_ponto` smallint(6) NOT NULL,
  `passagem_horarios` time NOT NULL,
  PRIMARY KEY (`id_horario`),
  KEY `horarios_ibfk_1` (`id_ponto`),
  CONSTRAINT `horarios_ibfk_1` FOREIGN KEY (`id_ponto`) REFERENCES `pontos` (`id_pontos`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB AUTO_INCREMENT=438 DEFAULT CHARSET=utf8;

CREATE TABLE `avaliacao` (
  `id_avaliacao` int(11) NOT NULL AUTO_INCREMENT,
  `id_usuario` int(11) DEFAULT NULL,
  `id_motorista` int(11) NOT NULL,
  `nota_avaliacao` tinyint(4) NOT NULL,
  `comentario_avaliacao` varchar(255) DEFAULT NULL,
  `data_avaliacao` datetime NOT NULL,
  PRIMARY KEY (`id_avaliacao`),
  KEY `fk_avaliacao_motorista` (`id_motorista`),
  KEY `fk_avaliacao_usuario` (`id_usuario`),
  CONSTRAINT `fk_avaliacao_motorista` FOREIGN KEY (`id_motorista`) REFERENCES `motorista` (`id_motorista`) ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT `fk_avaliacao_usuario` FOREIGN KEY (`id_usuario`) REFERENCES `usuarios` (`id_usuario`) ON DELETE SET NULL ON UPDATE CASCADE
) ENGINE=InnoDB AUTO_INCREMENT=33 DEFAULT CHARSET=utf8;

CREATE TABLE `motoristas_rotas` (
  `id_motorista_rota` int(11) NOT NULL AUTO_INCREMENT,
  `id_motorista` int(11) NOT NULL,
  `id_rota` mediumint(9) NOT NULL,
  PRIMARY KEY (`id_motorista_rota`),
  UNIQUE KEY `uk_motoristas_rotas` (`id_motorista`,`id_rota`),
  KEY `fk_motoristas_rotas_rota` (`id_rota`),
  CONSTRAINT `fk_motoristas_rotas_motorista` FOREIGN KEY (`id_motorista`) REFERENCES `motorista` (`id_motorista`) ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT `fk_motoristas_rotas_rota` FOREIGN KEY (`id_rota`) REFERENCES `rotas` (`id_rota`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB AUTO_INCREMENT=52 DEFAULT CHARSET=utf8;

